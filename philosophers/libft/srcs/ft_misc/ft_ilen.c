#include "ft_misc.h"

size_t		ft_ilen(t_lli n)
{
	size_t i;

	i = 1;
	while (!((n < 10) && (n > -10)))
	{
		n = n / 10;
		i++;
	}
	return ((n < 0) ? i + 1 : i);
}
