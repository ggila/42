#include "ft_str.h"

size_t		ft_strlen_func(char *s, int (*f)(char))
{
	size_t		i;

	if (!s || !f)
		return (0);
	i = 0;
	while (s[i])
	{
		if (f(s[i]))
			break ;
		i++;
	}
	return (i);
}
